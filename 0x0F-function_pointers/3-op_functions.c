#include "3-calc.h"
/**
 * op_add - adds a function
 * @a: number 1
 * @b: number 2
 *
 * Return: Sum
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substracts a two numbers
 * @a: number 1
 * @b: number 2
 *
 * Return: Substract
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two numbers
 * @a: number 1
 * @b: number 2
 *
 * Return: multiple
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers
 * @a: number 1
 * @b: number 2
 *
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - divides two numbers
 * @a: number 1
 * @b: number 2
 *
 * Return: reminder of division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
